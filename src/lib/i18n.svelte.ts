function detect(): LangCode {
	if (!browser) return 'en';
	const saved = localStorage.getItem(KEY);
	if (saved === 'en' || saved === 'zh' || saved === 'zh-Hant' || saved === 'ja') return saved;
	const nav = (navigator.language || '').toLowerCase();
	if (nav.startsWith('ja')) return 'ja';
	if (nav.startsWith('zh'))
		return nav.includes('tw') || nav.includes('hk') || nav.includes('hant') ? 'zh-Hant' : 'zh';
	return 'en';
}

class Locale {
	code = $state<LangCode>(detect());

	get dict(): Dict {
		switch (this.code) {
			case 'zh':
				return zh;
			case 'zh-Hant':
				return zhHant;
			case 'ja':
				return ja;
			default:
				return en;
		}
	}
	get tag(): string {
		switch (this.code) {
			case 'zh':
				return 'zh-CN';
			case 'zh-Hant':
				return 'zh-TW';
			case 'ja':
				return 'ja-JP';
			default:
				return 'en-US';
		}
	}
	set(code: LangCode) {
		this.code = code;
		if (browser) localStorage.setItem(KEY, code);
	}
}

export const locale = new Locale();

/** Translate a key, applying args when the entry is a function. */
export function t(key: string, ...args: unknown[]): string {
	const entry = (locale.dict[key] ?? en[key]) as Entry | undefined;
	if (entry === undefined) return key;
	return typeof entry === 'function' ? entry(...args) : entry;
}
